            htmlData += `
              <div class="post-item">
                <img src="<?= BLOG_THUMB_URL ?>${val.thumbnail_img}" alt="" class="flex-shrink-0" style="width: 80px; margin-right: 15px;">
                <div>
                  <h4><a href="blog-details.html?id=${val.blog_id}">${val.blog_title}</a></h4>
                  <time datetime="${formatDate(val.created_at)}">${formatDate(val.created_at)}</time>
                </div>
              </div><!-- End recent post item-->            
            `;
          });
        }
        $('#recent_posts').empty();
        $('#recent_posts').html(htmlData);
      },
      error: function(response) {
        console.log('>>>>>  ' + response);
      }
    });

  }

  // Loads the category and authors
  function loadFilterItem(reqType) {
    $.ajax({
      type: "POST",
      url: "./Apis/load.php",
      data: {
        reqType
      },
      success: function(response) {
        const res = JSON.parse(response);
        var htmlData = '';
        if (res.statusCode === 200) {
          if (reqType == 'category_item') {
            $(res.data).each(function(index, val) {
              htmlData += `<li><a href="#" id="${val.blog_cat_id}">${val.blog_cat_name} <span>(${val.blog_count})</span></a></li>`;
            });

            $('#category_items ul').empty();
            $('#category_items ul').html(htmlData);
          }
          if (reqType == 'author_item') {
            $(res.data).each(function(index, val) {
              htmlData += `<li><a href="#" id="${val.author_id}">${val.author_name}</a></li>`;
            });
            $('#author_items ul').empty();
            $('#author_items ul').html(htmlData);

          }
        }

      },
      error: function(response) {
        console.log('>>>>>  ' + response);
      }
    });

  }
