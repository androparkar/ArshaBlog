  // formats the date(needs improvement)
  function formatDate(date) {
    const parts = date.split(' ')
    return parts[0]; // yyyy-mm-dd
  }

  //Main data function
  function getData({
    reqType,
    page_id = 1,
    limit = 6,
    author_id,
    category_id,
    search_token = ''

  }) {


    $.ajax({
      type: "POST",
      url: "./Apis/load.php",
      data: {
        reqType,
        page_id,
        limit,
        author_id,
        category_id,
        search_token
      },
      success: function(response) {
        const res = JSON.parse(response);
        let htmlData = '';
        let recentHtmlData = '';

        loadPagination(res.row, page_id);

        if (res.statusCode === 200) {
          if (res.data && res.data.length > 0) {
            $(res.data).each(function(index, val) {
              htmlData += `
                <div class="col-lg-6">
                  <article>

                    <div class="post-img">
                      <img src="<?= BLOG_THUMB_URL ?>${val.thumbnail_img}" alt="thumbnail image" class="img-fluid">
                    </div>

                    <h2 class="title">
                      <a href="blog-details.html?blog_id=${val.blog_id}">${val.blog_title}</a>
                    </h2>

                    <div class="meta-top">
                      <ul>
                        <li class="d-flex align-items-center"><i class="bi bi-person"></i> <a href="blog-details.html">${val.author_name}</a></li>
                        <li class="d-flex align-items-center"><i class="bi bi-clock"></i> <a href="blog-details.html"><time datetime="${formatDate(val.created_at)}">${formatDate(val.created_at)}</time></a></li>
                        <!-- <li class="d-flex align-items-center"><i class="bi bi-chat-dots"></i> <a href="blog-details.html">12 Comments</a></li> -->
                      </ul>
                    </div>

                    <div class="content">
                      ${formatContent(val.blog_text)}
                      <div class="read-more">
                        <a href="blog-details.php?blog_id=${val.blog_id}">Read More</a>
                      </div>
                    </div>

                  </article>
                </div><!-- End post list item -->
              `;
            });
            // Set Main blog data 
            $('#blog_items').empty();
            $('#blog_items').html(htmlData);
          } else {
            // No data â€” show message(needs improvement)
            console.log('>>>>>  ');
            $('#blog_items').html('<p>No blog posts found.</p>');
          }
        }

      },
      error: function(response) {
        alert(response)
      }
    });
  }

  // trims the words at the end and adds `...`
  function formatContent(text) {

    let length = text.length;

    let trimmed = (length >= 120) ? text.slice(0, 120) + "..." : text;
    return trimmed;
  }

  // creates a pagination based on getData results
  function loadPagination(totalRows, page_id = 1, limit = 6) {

    console.log('>>>>>  ' + totalRows);
    console.log('>>>>>  ' + page_id);
    console.log('>>>>>  ' + limit);

    const totalPages = Math.ceil(totalRows / limit);
    const max = 4;
    let htmlData = '';
    // Left arrow
    if (page_id > 1) {
      htmlData += `<li><a href="#" id="${page_id - 1}"><i class="bi bi-chevron-left"></i></a></li>`;
    }

    for (let i = 1; i < max && i <= totalPages; i++) {
      let itemClass = (i === page_id) ? 'active' : '';
      htmlData += `<li><a href="#" class="${itemClass}" id="${i}">${i}</a></li>`;
    }

    // Ellipsis + last page
    if (totalPages > max) {
      htmlData += `<li><span>...</span></li>`;
      htmlData += `<li><a href="#" id="${totalPages}">${totalPages}</a></li>`;
    }

    // Right arrow
    if (page_id < totalPages) {
      htmlData += `<li><a href="#" id="${page_id + 1}"><i class="bi bi-chevron-right"></i></a></li>`;
    }

    $('#pagination ul').empty();
    $('#pagination ul').html(htmlData);
  }

  // loads recent posts
  function loadRecentPost(reqType, limit = 6) {
    $.ajax({
      type: "POST",
      url: "./Apis/load.php",
      data: {
        reqType,
        limit
      },
      success: function(response) {
