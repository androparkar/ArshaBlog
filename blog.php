    loadFilterItem('category_item');
    loadFilterItem('author_item');

    // Pagination control
    $(document).on('click', '#pagination a', function(e) {
      e.preventDefault();
      var page_id = Number($(this).attr('id'));
      getData({
        reqType: 'blog',
        page_id: page_id,
        category_id: category_id,
        author_id: author_id
      });
    });

    // Search 
    $(document).on('keyup', '#search_box', function(e) {
      e.preventDefault();
